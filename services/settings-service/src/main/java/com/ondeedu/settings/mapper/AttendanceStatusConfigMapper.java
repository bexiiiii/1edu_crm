package com.ondeedu.settings.mapper;

import com.ondeedu.settings.dto.AttendanceStatusConfigDto;
import com.ondeedu.settings.dto.SaveAttendanceStatusRequest;
import com.ondeedu.settings.entity.AttendanceStatusConfig;
import org.mapstruct.BeanMapping;
import org.mapstruct.Builder;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.MappingTarget;
import org.mapstruct.NullValuePropertyMappingStrategy;

@Mapper(componentModel = "spring", nullValuePropertyMappingStrategy = NullValuePropertyMappingStrategy.IGNORE)
public interface AttendanceStatusConfigMapper {

    AttendanceStatusConfigDto toDto(AttendanceStatusConfig entity);

    @BeanMapping(builder = @Builder(disableBuilder = true))
    @Mapping(target = "id", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "version", ignore = true)
    @Mapping(target = "systemStatus", ignore = true)
    AttendanceStatusConfig toEntity(SaveAttendanceStatusRequest request);

    @BeanMapping(builder = @Builder(disableBuilder = true))
    @Mapping(target = "id", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "version", ignore = true)
    @Mapping(target = "systemStatus", ignore = true)
    void updateEntity(@MappingTarget AttendanceStatusConfig entity, SaveAttendanceStatusRequest request);
}
